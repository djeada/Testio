"""
Defines the result renderer.

The result renderer is responsible for rendering the result of a comparison between
the expected output and the actual output of a tested program.

Machine-readable output (JSON, JUnit XML, TAP) is generated by the module-level
``render_*_report`` helpers and written by the ``run`` command when ``--format``
is not ``console``.
"""

import json
import xml.etree.ElementTree as ET
from io import StringIO
from typing import Any, Dict, List

from src.apps.cli.string_consts import COLOR_CODES, REPORT_MESSAGES
from src.core.execution.data import ComparisonOutputData, ComparisonResult

# Maps each ComparisonResult to its rendering configuration.
# Keys: color, header, show_input, show_expected, show_output, show_error
_RENDER_CONFIG: dict = {
    ComparisonResult.MATCH: {
        "color": COLOR_CODES.OK,
        "header": REPORT_MESSAGES.TEST_PASSED,
        "show_input": True,
        "show_expected": True,
        "show_output": True,
        "show_error": False,
    },
    ComparisonResult.MISMATCH: {
        "color": COLOR_CODES.FAIL,
        "header": REPORT_MESSAGES.TEST_FAILED,
        "show_input": True,
        "show_expected": True,
        "show_output": True,
        "show_error": False,
    },
    ComparisonResult.EXECUTION_ERROR: {
        "color": COLOR_CODES.FAIL,
        "header": REPORT_MESSAGES.ERROR,
        "show_input": True,
        "show_expected": False,
        "show_output": False,
        "show_error": True,
    },
    ComparisonResult.TIMEOUT: {
        "color": COLOR_CODES.FAIL,
        "header": REPORT_MESSAGES.TIMEOUT,
        "show_input": True,
        "show_expected": False,
        "show_output": False,
        "show_error": False,
    },
}


class ResultRenderer:
    """Renders the result of a comparison using a data-driven approach."""

    def render(self, comparison_output_data: ComparisonOutputData, i: int) -> None:
        """
        Renders the result of a comparison to stdout.

        :param comparison_output_data: The comparison data to render.
        :param i: The 1-based test index.
        :raises ValueError: If the comparison result is not recognised.
        """
        cfg = _RENDER_CONFIG.get(comparison_output_data.result)
        if cfg is None:
            raise ValueError(
                f"Unrecognised comparison result: {comparison_output_data.result!r}"
            )

        lines = [cfg["color"], f"#{i} {cfg['header']}"]

        if cfg["show_input"]:
            lines.append(
                f"Input: \n{comparison_output_data.input}\n"
                if comparison_output_data.input
                else REPORT_MESSAGES.NO_INPUT
            )

        if cfg["show_expected"]:
            lines.append(
                f"Expected output: \n{comparison_output_data.expected_output}\n"
                if comparison_output_data.expected_output
                else "No output was expected!"
            )

        if cfg["show_output"]:
            lines.append(
                f"Result: \n{comparison_output_data.output}\n"
                if comparison_output_data.output
                else "The program did not produce any output!"
            )

        if cfg["show_error"]:
            lines.append(f"Error: \n{comparison_output_data.error}\n")

        lines.append(COLOR_CODES.END)
        print("\n".join(lines))


# ---------------------------------------------------------------------------
# Machine-readable report formatters
# ---------------------------------------------------------------------------
# Each function accepts the ``all_results`` list built by ``run.execute()`` and
# the ``summary`` dict, and returns a formatted *string* ready to write to a
# file or stdout.
#
# ``all_results`` item shape:
#   {
#     "file": str,
#     "total_tests": int,
#     "passed_tests": int,
#     "pass_rate": float,
#     "tests": [{"input": str, "expected_output": str, "output": str,
#                "error": str, "result_name": str, "result": str}, ...]
#   }
# ``summary`` shape:
#   {"files_tested": int, "total_tests": int, "passed": int, "failed": int,
#    "pass_rate": float, "config_file": str}
# ---------------------------------------------------------------------------


def render_json_report(
    all_results: List[Dict[str, Any]], summary: Dict[str, Any]
) -> str:
    """Return a pretty-printed JSON string of the full test run."""
    return json.dumps({"summary": summary, "results": all_results}, indent=2)


def render_junitxml_report(
    all_results: List[Dict[str, Any]], summary: Dict[str, Any]
) -> str:
    """Return a JUnit-compatible XML string.

    Compatible with GitHub Actions, Jenkins, and other CI systems that consume
    JUnit XML (``junit.xml`` / ``**/test-results.xml``).
    """
    testsuites = ET.Element(
        "testsuites",
        {
            "name": "testio",
            "tests": str(summary["total_tests"]),
            "failures": str(summary["failed"]),
            "errors": "0",
            "time": "0",
        },
    )

    for file_result in all_results:
        file_failures = file_result["total_tests"] - file_result["passed_tests"]
        ts = ET.SubElement(
            testsuites,
            "testsuite",
            {
                "name": file_result["file"],
                "tests": str(file_result["total_tests"]),
                "failures": str(file_failures),
                "errors": "0",
                "time": "0",
            },
        )
        classname = file_result["file"].replace("/", ".").replace("\\", ".")
        for i, test in enumerate(file_result["tests"]):
            tc = ET.SubElement(
                ts,
                "testcase",
                {
                    "name": f"Test #{i + 1}",
                    "classname": classname,
                    "time": "0",
                },
            )
            if test.get("result_name") != "MATCH":
                failure = ET.SubElement(
                    tc,
                    "failure",
                    {"message": test.get("result_name", "FAILED")},
                )
                failure.text = (
                    f"Expected:\n{test.get('expected_output', '')}\n"
                    f"Got:\n{test.get('output', '')}"
                )
                if test.get("error"):
                    failure.text += f"\nError:\n{test['error']}"

    ET.indent(testsuites)
    buf = StringIO()
    tree = ET.ElementTree(testsuites)
    tree.write(buf, encoding="unicode", xml_declaration=True)
    return buf.getvalue()


def render_tap_report(
    all_results: List[Dict[str, Any]], summary: Dict[str, Any]
) -> str:
    """Return a TAP version 13 string.

    TAP (Test Anything Protocol) is consumed by tap-spec, tap-junit, and many
    CI/CD tools.  Each test becomes a single ``ok`` / ``not ok`` line with
    optional YAML diagnostic block.
    """
    lines = ["TAP version 13", f"1..{summary['total_tests']}"]
    test_num = 1
    for file_result in all_results:
        for i, test in enumerate(file_result["tests"]):
            passed = test.get("result_name") == "MATCH"
            status = "ok" if passed else "not ok"
            lines.append(
                f"{status} {test_num} - {file_result['file']} :: Test #{i + 1}"
            )
            if not passed:
                lines.append("  ---")
                if test.get("expected_output"):
                    lines.append(f"  expected: {test['expected_output']!r}")
                if test.get("output"):
                    lines.append(f"  got:      {test['output']!r}")
                if test.get("error"):
                    lines.append(f"  error:    {test['error']!r}")
                lines.append("  ...")
            test_num += 1
    return "\n".join(lines)
